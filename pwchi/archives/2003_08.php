<h2 class="date">August 30, 2003</h2>


<div class="blogbody">
<a name="000495"></a>
<h3 class="title">gaim - Will we be blocked?</h3>

<p>數天前，gaim 網站發表了一篇針對『微軟將於 10/15 以後切斷第三方 MSN IM 軟體連線』的回應。</p>

<p>由於許多使用者都向 gaim 詢問，10/15 以後是否還能使用 MSN 服務。</p>

<p>整篇文章看下來，目前問題的重點不再"技術問題"，而是"授權問題"。</p>

<p>其實要相容 MSN Protocol v8 和新版的 v9，這些對 gaim 並不是太大的技術性問題。但是微軟希望所有第三方軟體商都取得合法授權，因此目前也無法肯定回答 gaim 到時是否能繼續連線 MSN。如果屆時事情真的發生， gaim 將會捨棄對 MSN 的支援。</p>

<p>所以大家要有心理準備。</p>

<p><a href="http://gaim.sourceforge.net/msn.php">MSN Information - Will we be blocked?</a></p>


