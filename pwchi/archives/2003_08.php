<div class="blogbody">
<a name="000492"></a>
<h3 class="title">Docbook 文件製作環境</h3>

<p>要在 Linux 下建立一個 Docbook 的出版平台，似乎比 Windows 容易許多...</p>

<p>Docbook 是文件出版用的描述語言標準，Docbook 最初以 SGML 為主要描述語言，到 Docbook 4.1 後加入 XML 的支援，並開始以 XML 為建議使用的描述語言。使用 Docbook 的好處在於，一份使用 Docbook 製作的文件檔，可以輸出成多分不同的出版格式，包括 plain text，HTML，PDF...等等。而且根據不同場合需要，只要變換 Stylesheets ，就可達成不同的顯示樣式。換句話說，Docbook 提供的是文件的內容，但並不描述樣式，文件該長什麼樣子，是由 Stylesheets 來描述。<br />
要建立一個 Docbook 文件的出版環境，需要三樣東西，Docbok DTD，Docbook Stylesheets 和 XSLT Engine。有了這三樣東西，就可以製作交換行強的文件檔案。</p>


