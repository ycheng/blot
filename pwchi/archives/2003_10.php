<h3 class="title">單面雙層的 DVD-9 燒錄規格</h3>

<p>近期 "Pioneer" 與 "DVD+RW 聯盟" 都分別發布令人振奮的好消息 ...</p>

<p>DVD-RW 大廠 <a href="http://www.watch.impress.co.jp/av/docs/20031003/pioneer.htm">Pioneer</a> 與 <a href="http://www.dvdrw.com/press/duallayer.htm">DVD+RW</a> 組織，都在最近發表了 DVD-9 的單面雙層標準，兩者最大容量都可達 8.5 GB，且均相容現階段的 DVD-ROM 光碟機。</p>

<p>雖然已經有 4.7GB 的 DVD 燒錄機，但是許多 DVD 影片，均採用 DVD-9 的格式。想要合法備份的使用者，常常需要自行切割，分片，造成相當的麻煩。如今，新的 DVD-9 燒錄機，即將登場。相信這對許多人來說，是個相當興奮的好消息。</p>

<p>支援 DVD-9 的 DVD+RW 燒錄機，預計 2004 年上市!</p>


